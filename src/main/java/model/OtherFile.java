package model;

public class OtherFile {

    private int jobId;
    private String name;
    private byte[] file;
    private String extension;

    public OtherFile(int jobId, String name, byte[] file, String extension) {
        this.jobId = jobId;
        this.name = name;
        this.file = file;
        this.extension = extension;
    }

}
